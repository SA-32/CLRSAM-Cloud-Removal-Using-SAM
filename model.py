## Resizing modules
class Downsample(nn.Module):
    def __init__(self, n_feat):
        super(Downsample, self).__init__()

        self.body = nn.Sequential(nn.Conv2d(n_feat, n_feat//2, kernel_size=3, stride=1, padding=1, bias=False),
                                  nn.PixelUnshuffle(2))

    def forward(self, x):
        return self.body(x)

class Upsample(nn.Module):
    def __init__(self, n_feat):
        super(Upsample, self).__init__()

        self.body = nn.Sequential(nn.Conv2d(n_feat, n_feat*2, kernel_size=3, stride=1, padding=1, bias=False),
                                  nn.PixelShuffle(2))

    def forward(self, x):
        return self.body(x)

class ImageDecoderViT(nn.Module):
  def __init__(self, num_blocks = [4,6,6,8],
        num_refinement_blocks = 4,
        heads = [1,2,4,8],
        ffn_expansion_factor = 2.66,
        bias = False,
        LayerNorm_type = 'WithBias'):
    super().__init__()
    self.decoder_level4 = nn.Sequential(*[TransformerBlock(dim=int(256), num_heads=heads[3], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type) for i in range(num_blocks[3])])

    self.up4_3 = Upsample(int(256)) ## From Level 4 to Level 3
    self.decoder_level3 = nn.Sequential(*[TransformerBlock(dim=int(128), num_heads=heads[2], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type) for i in range(num_blocks[2])])

    self.up3_2 = Upsample(int(128)) ## From Level 3 to Level 2
    self.decoder_level2 = nn.Sequential(*[TransformerBlock(dim=int(64), num_heads=heads[1], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type) for i in range(num_blocks[1])])

    self.output = nn.Conv2d(64, 3, kernel_size=3, stride=1, padding=1, bias=bias)

  def forward(self, x):
    x = self.decoder_level4(x)

    x = self.up4_3(x)
    x = self.decoder_level3(x)

    x = self.up3_2(x)
    x = self.decoder_level2(x)

    x = self.output(x)

class NeuralNet(nn.Module):
  def __init__(self, ):
    super(NeuralNet, self).__init__()
    self.encoder = ImageEncoderViT()
    self.decoder = ImageDecoderViT()

  def forward(self, x):
    x = self.encoder(x)
    x = self.decoder(x)

    return x

